// Widening TypeCasting

public class Widening {
    public static void main(String[] args) {
        int x = 7;

        long y= x;

        double z= y;

        System.out.println("Before Convrsion : " +x);
        System.out.println("After Conversion : Long Conversion : "+y);
        System.out.println("After Conversion : Double Conversion : "+z);

    }
}
